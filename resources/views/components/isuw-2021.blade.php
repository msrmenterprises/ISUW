--><p style="text-align:justify;">The lockdown and remote working post-Covid-19 has underscored the importance of reliable
electricity supply. Everybody has realized that electricity is the most vital service in today’s
digital world! Electric utilities around the world have been striving hard to provide this essential
lifeline to the entirety of humanity fighting the ongoing pandemic. The way we work and live
has dramatically changed in the last ten months with the majority of the workforce in most sectors
working remotely or work from home (WFH). The operations and work environment of electric
(and other) utilities have also changed in this short span. In case of utilities which used to do
monthly meter reading manually and present the hard copy of the bills for payment are unable
to do so post Covid-19; and is posing serious problems in their revenue realization. This
situation is common in most of the developing countries where smart metering infrastructure is
not in place yet with electricity, water and gas utilities.</p>
<p style="text-align:justify;">Highly disruptive black swan events like Covid-19 also present never before opportunities for
innovation and transformation with profound implications that are gainful in the long term.
Electric utilities and other businesses which operated with hardcopies of every document have
suddenly changed to totally “paperless” environment. This change is irreversible and the pace
of digitalization in all businesses is going to be much faster. Emerging technologies like Artificial
Intelligence, Machine Learning and Advanced Analytics, Blockchain, Virtual Reality and
Augmented Reality, Drones and Robotic Process Automation are also being leveraged by
utilities for various applications which is optimizing business models and opening up new
business opportunities.</p>
<p style="text-align:justify;">Since inception in 2011, India Smart Grid Forum (ISGF) has been spearheading the movement
towards digitalization of utilities in India. Although, the actual implementation in many utilities
may still be in its infancy, there is already a unanimous voice for taking this journey to full
digitalization. In the aftermath of Covid-19, digital platforms have become the coveted assets
for utilities in their business continuity and resiliency. Government of India has recently
launched a new program that mandates smart meters for all the 250 million+ electricity
customers in the country. Besides smart metering, utilities are expected to leverage digital
platforms for many of their operations. All these new initiatives towards digitalization which in
the business as usual scenario in the pre-Covid world would have taken well over a decade are
now going to happen in the next 2-3 years on fast track. This is going to create data driven
smart utilities which will open up new business opportunities for organizations providing tools
and services to host and manage the enormous amounts of data utilities are expected to
generate in the coming days.</p>
<p style="text-align:justify;">ISGF has been organising its flagship annual event, India Smart Utility Week (ISUW) since 2015
and it is considered as one of the top five international events on Smart Grids and Smart Cities.
Post Covid-19, water and city gas distribution utilities are also preparing to equip themselves
with latest technologies for improved operational efficiency and successful integration into the
smart city systems. However,
lack of effective policy and regulations both at the central and state level along with inadequate
safety guidelines, standards, business models act as barriers for its growth. All the previous
editions of ISUW (formerly known as India Smart Grid Week – ISGW) were huge success that
attracted the attention and participation of the global community. Technology Companies,
Regulators, Policy Makers, Government Officials and Senior Officials from Electric, Water and
Gas Utilities from 50+ countries participated every year in the past editions of ISUW. Despite
the Covid-19 fear glooming over, ISUW 2020 was attended by over 1570+ delegates and 269
speakers from 23 countries which was conducted in March 2020 with utmost care and safety
precautions.</p>
<p style="text-align:justify;">ISUW 2021 has been scheduled from 02 – 05 March 2021 and it will be conducted on a
DIGITAL PLATFORM as an International Conference and Exhibition on Smart Energy and
Mobility for Smarter Cities. The 3-D Exhibition Booths at ISUW 2021 will offer never before
experience to exhibitors and visitors. ISUW 2021 will virtually bring together India’s leading
Electricity, Gas and Water Utilities, Policy Makers, Regulators, Investors and world’s top-
notch Smart Energy Experts and Researchers to discuss trends, share best practices and
showcase next generation technologies and products in smart energy and smart cities
domains. ISUW 2021 will include plenaries, interactive workshops, keynotes, technical
sessions and technical paper presentations. Bi-lateral Smart Grid workshop with EU, USA,
Switzerland, Sweden, France, Canada are also expected to be a part of the upcoming edition.
5 th edition of ISGF Innovation Awards 2021 will also be organised as part of ISUW 2021.</p>
