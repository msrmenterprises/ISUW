<p style="text-align:justify;">India has set the Net Zero target for 2070. Electrification being the key strategy for decarbonization,
for India to be Net Aero by 2070, the power sector in India should target to become Net Zero by
2050. Towards this goal, Government of India has launched a host of programs and projects that
comprises of: <br/>
<ul style="margin-left:10px;">
	<li>More than doubling the resent Power Generation Capacity to 900 GW by 2032</li>
	<li>Increasing the Renewable Energy (RE) based capacity to 596 GW by 2032</li>
	<li>Development of 236 GWh of Battery Energy Storage Systems (BESS) by 2032</li>
	<li>Launch of Carbon Market by end of 2023</li>
	<li>Rollout of 250 million Smart Meters on fast track</li>
	<li>Implementation of Time-of-Day (TOD) tariff for Electricity for all Consumers from 2025 which
	will incentivize consumption during high solar hours at reduced tariffs</li>
	<li>Launch of 10 million Rootops Solar PV Systems</li>
</ul>
</p>

<p style="text-align:justify;">Since inception in 2011, India Smart Grid Forum (ISGF) has been spearheading the movement
towards digitalization of utilities in India; and have played pivotal role in bringing new technologies
and solutions to India including Artificial Intelligence (AI), Machine Learning (ML), Robotics,
Blockchain, Web 3.0 and Metaverse applications. </p>
<p style="text-align:justify;">ISGF has been organising its flagship annual event, India Smart Utility Week (ISUW) since 2015 and it
is considered as one of the top five international events on Smart Grids, Electric Mobility and Smart
Energy. All the previous editions of ISUW (initially known as India Smart Grid Week – ISGW) were
huge success that attracted the attention and participation of the whose-who amongst top-notch
thinkers and utility leaders from around the globe. Technology Companies, Regulators, Policy
Makers, Government Officials and Senior Officers from Electricity, Water and Gas Utilities from 50+
countries participated every year in the past editions. ISUW 2023 was attended by over 2200
delegates and addressed by 264 eminent speakers from 22+ countries.</p>
